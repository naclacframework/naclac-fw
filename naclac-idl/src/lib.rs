use std::path::PathBuf;
use serde::{Serialize, Deserialize};
use serde_json::Value;

#[derive(Serialize, Deserialize, Clone)]
pub struct Idl {
    pub address: String,
    pub is_zero_copy: bool,
    pub metadata: IdlMetadata,
    pub instructions: Vec<IdlInstruction>,
    pub accounts: Vec<IdlAccountStruct>,
    pub events: Vec<IdlEvent>,
    pub errors: Vec<IdlError>,
    pub constants: Vec<IdlConstant>,
    pub types: Vec<IdlType>,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlType {
    pub name: String,
    #[serde(rename = "type")]
    pub ty: IdlTypeDef,
}

#[derive(Serialize, Deserialize, Clone)]
#[serde(tag = "kind")]
pub enum IdlTypeDef {
    #[serde(rename = "struct")]
    Struct { fields: Vec<IdlField> },
    #[serde(rename = "enum")]
    Enum { variants: Vec<IdlEnumVariant> },
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlEnumVariant {
    pub name: String,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlMetadata {
    pub name: String,
    pub version: String,
    pub description: String,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlInstruction {
    pub name: String,
    pub discriminator: [u8; 8],
    pub accounts: Vec<IdlAccount>,
    pub args: Vec<IdlField>,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlAccount {
    pub name: String,
    pub writable: bool,
    pub signer: bool,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub pda: Option<IdlPda>,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub address: Option<String>,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlPda {
    pub seeds: Vec<IdlSeed>,
}

#[derive(Serialize, Deserialize, Clone)]
#[serde(tag = "kind")]
pub enum IdlSeed {
    #[serde(rename = "const")]
    Const { 
        value: Vec<u8>,
        #[serde(skip_serializing_if = "Option::is_none")]
        name: Option<String>
    },
    #[serde(rename = "arg")]
    Arg { path: String },
    #[serde(rename = "account")]
    Account { path: String },
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlField {
    pub name: String,
    #[serde(rename = "type")]
    pub ty: Value,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlAccountStruct {
    pub name: String,
    #[serde(rename = "type")]
    pub ty: IdlTypeStruct,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlTypeStruct {
    pub kind: String,
    pub fields: Vec<IdlField>,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlEvent {
    pub name: String,
    pub fields: Vec<IdlEventField>,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlEventField {
    pub name: String,
    #[serde(rename = "type")]
    pub ty: Value,
    pub index: bool,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlError {
    pub code: u32,
    pub name: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub msg: Option<String>,
}

#[derive(Serialize, Deserialize, Clone)]
pub struct IdlConstant {
    pub name: String,
    #[serde(rename = "type")]
    pub ty: Value,
    pub value: String,
}

pub fn generate_idl(
    program_dir: &PathBuf, 
    program_name: &str, 
    address: &str, 
    version: &str,
    is_zero_copy: bool
) -> Result<String, Box<dyn std::error::Error>> {
    let ast = naclac_syn::parse_workspace_program(program_dir, program_name, is_zero_copy);
    
    let metadata = IdlMetadata {
        name: program_name.to_string(),
        version: version.to_string(),
        description: format!("{} — Generated by Naclac Framework", program_name),
    };

    let mut used_constants = std::collections::HashSet::new();

    let mut instructions = Vec::new();
    for ix in &ast.instructions {
        let mut idl_accounts = Vec::new();
        for acc in &ix.accounts {
            let pda = if let Some(pda) = &acc.pda {
                let mut seeds = Vec::new();
                for seed in &pda.seeds {
                    match seed {
                        naclac_syn::types::NaclacSeed::Const { value, name } => {
                            if let Some(n) = name {
                                used_constants.insert(n.clone());
                            }
                            seeds.push(IdlSeed::Const { 
                                value: value.clone(),
                                name: name.clone(),
                            });
                        }
                        naclac_syn::types::NaclacSeed::Arg { path } => {
                            seeds.push(IdlSeed::Arg { path: path.clone() });
                        }
                        naclac_syn::types::NaclacSeed::Account { path } => {
                            seeds.push(IdlSeed::Account { path: path.clone() });
                        }
                    }
                }
                Some(IdlPda { seeds })
            } else {
                None
            };
            idl_accounts.push(IdlAccount {
                name: acc.name.clone(),
                writable: acc.writable,
                signer: acc.signer,
                pda,
                address: acc.address.clone(),
            });
        }
        
        let mut idl_args = Vec::new();
        for arg in &ix.args {
            idl_args.push(IdlField {
                name: arg.name.clone(),
                ty: arg.ty.clone(),
            });
        }
        
        instructions.push(IdlInstruction {
            name: ix.name.clone(),
            discriminator: ix.discriminator,
            accounts: idl_accounts,
            args: idl_args,
        });
    }

    let mut accounts = Vec::new();
    for acc in &ast.accounts {
        let mut fields = Vec::new();
        for f in &acc.fields {
            fields.push(IdlField {
                name: f.name.clone(),
                ty: f.ty.clone(),
            });
        }
        accounts.push(IdlAccountStruct {
            name: acc.name.clone(),
            ty: IdlTypeStruct {
                kind: "struct".to_string(),
                fields,
            },
        });
    }

    let mut events = Vec::new();
    for evt in &ast.events {
        let mut fields = Vec::new();
        for f in &evt.fields {
            fields.push(IdlEventField {
                name: f.name.clone(),
                ty: f.ty.clone(),
                index: f.index,
            });
        }
        events.push(IdlEvent {
            name: evt.name.clone(),
            fields,
        });
    }

    let mut errors = Vec::new();
    for err in &ast.errors {
        errors.push(IdlError {
            code: err.code,
            name: err.name.clone(),
            msg: err.msg.clone(),
        });
    }

    let mut constants = Vec::new();
    for constant in &ast.constants {
        if constant.is_exported || used_constants.contains(&constant.name) {
            constants.push(IdlConstant {
                name: constant.name.clone(),
                ty: constant.ty.clone(),
                value: constant.value.clone(),
            });
        }
    }

    let mut types = Vec::new();
    for t in &ast.types {
        let ty = match &t.ty {
            naclac_syn::types::NaclacTypeDefTy::Struct { fields } => {
                let mut idl_fields = Vec::new();
                for f in fields {
                    idl_fields.push(IdlField {
                        name: f.name.clone(),
                        ty: f.ty.clone(),
                    });
                }
                IdlTypeDef::Struct { fields: idl_fields }
            }
            naclac_syn::types::NaclacTypeDefTy::Enum { variants } => {
                let mut idl_variants = Vec::new();
                for v in variants {
                    idl_variants.push(IdlEnumVariant { name: v.name.clone() });
                }
                IdlTypeDef::Enum { variants: idl_variants }
            }
        };
        types.push(IdlType {
            name: t.name.clone(),
            ty,
        });
    }

    let idl = Idl {
        address: address.to_string(),
        is_zero_copy,
        metadata,
        instructions,
        accounts,
        events,
        errors,
        constants,
        types,
    };

    Ok(serde_json::to_string_pretty(&idl)?)
}
